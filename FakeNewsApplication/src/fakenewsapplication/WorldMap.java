    public int[][] getMap() {
        return map;
    }
    
    public void setMap(int code, int x, int y) {
        map[x][y] = code;
    }
    
    public boolean isImmunable(People people) {
        return isCloserArea(2, people);
    }
    
    public boolean isInfectable(People people) {
        return isCloserArea(3, people);
    }
    
    public boolean isUnfectable(People people) {
        return isCloserArea(4, people);
    }
    
    private boolean isCloserArea(int areaCode, People people) {
        int x = people.getX();
        int y = people.getY();
        
        boolean isExaclyPosition = map[x][y] == areaCode;
        
        boolean isNotInYUpperLimit = y < MAP_TOTAL_COLUNMS - 1;
        boolean isNotInYLowerLimit = y - 1 >= 0;
        boolean isVerticalUpperImmunity = isNotInYUpperLimit ? map[x][y + 1] == areaCode : false;
        boolean isVerticalLowerImmunity = isNotInYLowerLimit ? map[x][y - 1] == areaCode : false;
        
        boolean isNotInXUpperLimit = x < MAP_TOTAL_ROWS - 1;
        boolean isNotInXLowerLimit = x - 1 >= 0;
        boolean isHorizontalUpperImmunity = isNotInXUpperLimit ? map[x + 1][y] == areaCode : false;
        boolean isHorizontalLowerImmunity = isNotInXLowerLimit ? map[x - 1][y] == areaCode : false;
        
        return isExaclyPosition || isVerticalUpperImmunity || isVerticalLowerImmunity || isHorizontalUpperImmunity || isHorizontalLowerImmunity;
    }
